    public void limpar(){
        textNome.setText("");
        textCPF.setText("");
        textSalario.setText("");
        textDuraContrato.setText("");
        textValorContrato.setText("");
        textCNPJ.setText("");
        textIdade.setText("");
    }
    
    
    public void registrarPj() throws CpfInvalidoException, IdadeInvalidoException, SalarioInvalidoException {
    FuncionarioPJ funcPj = new FuncionarioPJ();
    
    funcPj.setNome(textNome.getText());
    
    String cpfStr = textCPF.getText();
    int cpf = Integer.parseInt(cpfStr);
    if (cpf < 0) {
        throw new CpfInvalidoException("CPF não pode ser negativo");
    }
    funcPj.setCpf(cpf);
    
    int idade = Integer.parseInt(textIdade.getText());
    if (idade <= 0) {
        throw new IdadeInvalidoException("Idade inválida");
    }
    funcPj.setIdade(idade);
    
    int salario = Integer.parseInt(textSalario.getText());
    if (salario < 0) {
        throw new SalarioInvalidoException("Salário inválido");
    }
    funcPj.setSalario(salario);
    
    
    int valorContrato = Integer.parseInt(textValorContrato.getText());
    funcPj.setValorContrato(valorContrato);
    
   
    int tempoContrato = Integer.parseInt(textDuraContrato.getText());
    funcPj.setTempoContrato(tempoContrato);
    
    
    String cnpj = textCNPJ.getText();
    funcPj.setCnpj(cnpj);
    
    
    comandos.registrarFuncionario(funcPj);
    
    JOptionPane.showMessageDialog(null, "Funcionário registrado com sucesso!", 
                                  "Cadastro de Funcionário", JOptionPane.INFORMATION_MESSAGE);
}

    
