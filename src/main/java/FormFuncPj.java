    private void BtCadastrar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtCadastrar1ActionPerformed
       try {
        registrarPj();
        int ret = JOptionPane.showConfirmDialog(
        null,
        "Funcionario PJ Registrado! Deseja cadastrar mais?",
        "Cadastro bem sucedido",
        JOptionPane.YES_NO_OPTION
    );
    if(ret == 0){
        limpar();
    } else {
            limpar();
            dispose();
    }
    } catch (CpfInvalidoException | IdadeInvalidoException | SalarioInvalidoException e) {
        JOptionPane.showMessageDialog(null, e.getMessage(), "Erro de Cadastro", JOptionPane.ERROR_MESSAGE);
    }  
    }//GEN-LAST:event_BtCadastrar1ActionPerformed

