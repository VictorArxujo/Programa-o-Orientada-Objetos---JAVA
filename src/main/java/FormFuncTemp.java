    public void registrarTemp() throws CpfInvalidoException, IdadeInvalidoException, SalarioInvalidoException {

    FuncionarioTemporario funcTemp = new FuncionarioTemporario();
    
    funcTemp.setNome(textNome.getText());  
    String cpfStr = textCPF.getText().trim();
    
    int cpf;
    
    try {
        cpf = Integer.parseInt(cpfStr); 
    } catch (NumberFormatException e) { // Ver se tem excessao para o Cpf
        throw new CpfInvalidoException("CPF deve ser um n√∫mero v√°lido.");
    }
    if (cpf < 0) {
        throw new CpfInvalidoException("CPF n√£o pode ser negativo");
    }
    funcTemp.setCpf(cpf);

    
    int idade;
    try {
        idade = Integer.parseInt(textIdade.getText().trim());
    } catch (NumberFormatException e) { //Ver se tem Excessao Para idade
        throw new IdadeInvalidoException("Idade deve ser um n√∫mero v√°lido.");
    }
    if (idade <= 0) {
        throw new IdadeInvalidoException("Idade inv√°lida. Deve ser maior que 0.");
    }
    funcTemp.setIdade(idade);

    
    double salario;
    try {
        salario = Double.parseDouble(textSalario.getText().trim());
    } catch (NumberFormatException e) { // Ver se tem Excessao Para idade
        throw new SalarioInvalidoException("Sal√°rio deve ser um n√∫mero v√°lido.");
    }
    if (salario < 0) {
        throw new SalarioInvalidoException("Sal√°rio inv√°lido. N√£o pode ser negativo.");
    }
    funcTemp.setSalario(salario);
    
    int vh = Integer.parseInt(textValorHoras.getText());
    funcTemp.setValorHora(vh);
    
    int ht = Integer.parseInt(textValorHoras.getText());
    funcTemp.sethorasTrabalhadas(ht);
    
    
    comandos.registrarFuncionario(funcTemp); // Registra o Funcionario
    
    JOptionPane.showMessageDialog(null, "Funcion√°rio registrado com sucesso!", "Cadastro de Funcion√°rio", JOptionPane.INFORMATION_MESSAGE);
}
    
