    private void BtCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtCadastrarActionPerformed
        try {
        registrarTemp();
        int ret = JOptionPane.showConfirmDialog(
        null,
        "Funcionario Temporario Registrado! Deseja cadastrar mais?",
        "Cadastro bem sucedido",
        JOptionPane.YES_NO_OPTION
    );
    if(ret == 0){
        limpar();
    } else {
            limpar();
            dispose();
    }
    } catch (CpfInvalidoException | IdadeInvalidoException | NumberFormatException e) {
        JOptionPane.showMessageDialog(null, e.getMessage(), "Erro de Cadastro", JOptionPane.ERROR_MESSAGE);
    }   catch (SalarioInvalidoException ex) {
            Logger.getLogger(FormFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BtCadastrarActionPerformed

